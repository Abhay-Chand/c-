//WAP to find whether it is vowel or consonent by taking input from user
#include<iostream>
using namespace std;
int main(){
    char character; //enter in small letter
    cin>>character;
    switch (character)
    {
    case 'a':{
        cout<<"vowel"<<endl;
        break;
    }
    case 'e':{
        cout<<"vowel"<<endl;
        break;
    }
    case 'i':{
        cout<<"vowel"<<endl;
        break;
    }
    case 'o':{
        cout<<"vowel"<<endl;
        break;
    }
    case 'u':{
        cout<<"vowel"<<endl;
        break;
    }
    default:
        cout<<"consonent"<<endl;
        break;
    }

}